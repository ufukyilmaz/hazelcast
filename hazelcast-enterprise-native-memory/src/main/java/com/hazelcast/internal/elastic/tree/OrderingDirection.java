package com.hazelcast.internal.elastic.tree;


/**
 * Specifies ordering direction to iterate over sorted off-heap storage
 * <p/>
 * ASC - ascending order
 * DESC - descending order
 */
public enum OrderingDirection {
    /**
     * Ascending order
     */
    ASC,

    /**
     * Descending order
     */
    DESC
}
